package com.yedam.java.example;

public class Calculator {

	//더하기
	int plus(int x, int y) {
		return x + y;
	}
	
	//빼기
	int minus(int x, int y) {
		return  x - y;
		
	}
	
	//곱하기
	int multiply(int x, int y) {
		return  x * y;
		
	}
	
	//나누기
	double divide(int x, int y) {
		return x / y;
		
	}
	
	
	
	
}
